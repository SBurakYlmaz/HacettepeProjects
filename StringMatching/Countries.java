public class Countries {
    private String country_name;
    private Float population;
    private Float area_total;
    private Float area_land;
    private Float area_water;
    private Float median_age_male;
    private Float median_age_female;
    private Float birth_rate;
    private Float death_rate;
    private Float literacy_female;
    private Float airports;

    public String getCountry_name() {
        return country_name;
    }

    public void setCountry_name(String country_name) {
        this.country_name = country_name;
    }

    public Float getPopulation() {
        return population;
    }

    public void setPopulation(Float population) {
        this.population = population;
    }

    public Float getArea_total() {
        return area_total;
    }

    public void setArea_total(Float area_total) {
        this.area_total = area_total;
    }

    public Float getArea_land() {
        return area_land;
    }

    public void setArea_land(Float area_land) {
        this.area_land = area_land;
    }

    public Float getArea_water() {
        return area_water;
    }

    public void setArea_water(Float area_water) {
        this.area_water = area_water;
    }

    public Float getMedian_age_male() {
        return median_age_male;
    }

    public void setMedian_age_male(Float median_age_male) {
        this.median_age_male = median_age_male;
    }

    public Float getMedian_age_female() {
        return median_age_female;
    }

    public void setMedian_age_female(Float median_age_female) {
        this.median_age_female = median_age_female;
    }

    public Float getBirth_rate() {
        return birth_rate;
    }

    public void setBirth_rate(Float birth_rate) {
        this.birth_rate = birth_rate;
    }

    public Float getDeath_rate() {
        return death_rate;
    }

    public void setDeath_rate(Float death_rate) {
        this.death_rate = death_rate;
    }

    public Float getLiteracy_female() {
        return literacy_female;
    }

    public void setLiteracy_female(Float literacy_female) {
        this.literacy_female = literacy_female;
    }

    public Float getAirports() {
        return airports;
    }

    public void setAirports(Float airports) {
        this.airports = airports;
    }
}
